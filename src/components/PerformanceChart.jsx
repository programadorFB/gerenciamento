import React, { useState, useMemo } from 'react';
import { LineChart, Line, XAxis, YAxis, CartesianGrid, Tooltip, Legend, ResponsiveContainer } from 'recharts';
import { MdTrendingUp } from 'react-icons/md';
import { FaChartLine, FaCoins } from 'react-icons/fa';
import styles from './PerformanceChart.module.css';

const PerformanceChart = ({ transactions, currentBalance, initialBalance }) => {
    const [dateFilter, setDateFilter] = useState('7days'); // realtime, 7days, 30days, 90days, all
    const [realtimeData, setRealtimeData] = useState([]);

    // Formatar moeda
    const formatCurrency = (value) => {
        return new Intl.NumberFormat('pt-BR', {
            style: 'currency',
            currency: 'BRL'
        }).format(value);
    };

    // AtualizaÃ§Ã£o em tempo real (a cada 10 segundos)
    React.useEffect(() => {
        if (dateFilter === 'realtime') {
            const interval = setInterval(() => {
                // ForÃ§a recÃ¡lculo dos dados
                setRealtimeData(prev => [...prev]);
            }, 10000); // Atualiza a cada 10 segundos

            return () => clearInterval(interval);
        }
    }, [dateFilter]);

    // Processar dados em tempo real (por hora)
    const processRealtimeData = React.useCallback((transactions, startDate, endDate) => {
        const todayTransactions = transactions
            .filter(tx => {
                const txDate = new Date(tx.created_at || tx.date);
                return txDate >= startDate && txDate <= endDate;
            })
            .sort((a, b) => new Date(a.created_at || a.date) - new Date(b.created_at || b.date));

        // Usar saldo atual e voltar
        let currentBal = currentBalance || 0;
        
        console.log('ðŸ”´ Tempo Real - Saldo Atual:', currentBal);

        // Se nÃ£o hÃ¡ transaÃ§Ãµes hoje, mostrar apenas o saldo atual
        if (todayTransactions.length === 0) {
            const now = new Date();
            return [{
                date: `${now.getHours().toString().padStart(2, '0')}:00`,
                balance: currentBal,
                dateFormatted: `${now.getHours().toString().padStart(2, '0')}:00`,
                transactionCount: 0,
                isRealtime: true
            }];
        }

        // Agrupar por hora
        const dataByHour = {};

        todayTransactions.forEach(tx => {
            const txDate = new Date(tx.created_at || tx.date);
            const hourKey = `${txDate.getHours().toString().padStart(2, '0')}:00`;

            if (!dataByHour[hourKey]) {
                dataByHour[hourKey] = {
                    hour: hourKey,
                    transactions: []
                };
            }

            dataByHour[hourKey].transactions.push(tx);
        });

        // Calcular saldo no inÃ­cio do dia subtraindo transaÃ§Ãµes de hoje
        let balanceAtStartOfDay = currentBal;
        todayTransactions.forEach(tx => {
            const amount = parseFloat(tx.amount) || 0;
            if (tx.type === 'deposit' || tx.type === 'gains') {
                balanceAtStartOfDay -= amount;
            } else if (tx.type === 'withdraw' || tx.type === 'losses') {
                balanceAtStartOfDay += amount;
            }
        });

        console.log('ðŸ”´ Saldo inÃ­cio do dia:', balanceAtStartOfDay);

        // Processar de frente para trÃ¡s
        let runningBalance = balanceAtStartOfDay;
        const sortedHours = Object.keys(dataByHour).sort();

        const chartData = sortedHours.map(hourKey => {
            const hourData = dataByHour[hourKey];
            
            hourData.transactions.forEach(tx => {
                const amount = parseFloat(tx.amount) || 0;
                if (tx.type === 'deposit' || tx.type === 'gains') {
                    runningBalance += amount;
                } else if (tx.type === 'withdraw' || tx.type === 'losses') {
                    runningBalance -= amount;
                }
            });

            return {
                date: hourKey,
                balance: runningBalance,
                dateFormatted: hourKey,
                transactionCount: hourData.transactions.length,
                isRealtime: true
            };
        });

        console.log('ðŸ”´ Saldo final tempo real:', chartData[chartData.length - 1]?.balance);

        return chartData;
    }, [initialBalance, currentBalance]);

    // Processar TODAS as transaÃ§Ãµes (modo ALL)
    const processAllTransactions = React.useCallback((transactions) => {
        const sortedTransactions = [...transactions].sort((a, b) => 
            new Date(a.created_at || a.date) - new Date(b.created_at || b.date)
        );

        const dataByDate = {};
        let runningBalance = initialBalance || 0;

        console.log('ðŸ“Š ALL - ComeÃ§ando da banca inicial:', runningBalance);

        sortedTransactions.forEach(tx => {
            const txDate = new Date(tx.created_at || tx.date);
            const dateKey = txDate.toISOString().split('T')[0];

            if (!dataByDate[dateKey]) {
                dataByDate[dateKey] = {
                    date: dateKey,
                    transactions: []
                };
            }

            dataByDate[dateKey].transactions.push(tx);
        });

        const chartData = Object.keys(dataByDate)
            .sort()
            .map(dateKey => {
                const dayData = dataByDate[dateKey];
                
                dayData.transactions.forEach(tx => {
                    const amount = parseFloat(tx.amount) || 0;
                    if (tx.type === 'deposit' || tx.type === 'gains') {
                        runningBalance += amount;
                    } else if (tx.type === 'withdraw' || tx.type === 'losses') {
                        runningBalance -= amount;
                    }
                });

                return {
                    date: dateKey,
                    balance: runningBalance,
                    dateFormatted: new Date(dateKey).toLocaleDateString('pt-BR', {
                        day: '2-digit',
                        month: '2-digit'
                    }),
                    transactionCount: dayData.transactions.length
                };
            });

        console.log('ðŸ“Š ALL - Saldo final:', runningBalance);
        return chartData;
    }, [initialBalance]);

    // FunÃ§Ã£o para calcular saldo ao longo do tempo
    const processChartData = useMemo(() => {
        if (!transactions || transactions.length === 0) {
            // Se nÃ£o hÃ¡ transaÃ§Ãµes, mostrar apenas o saldo atual
            console.log('ðŸ“Š GrÃ¡fico: Sem transaÃ§Ãµes, usando saldo atual:', currentBalance);
            return [{
                date: new Date().toISOString().split('T')[0],
                balance: currentBalance || 0,
                dateFormatted: new Date().toLocaleDateString('pt-BR', {
                    day: '2-digit',
                    month: '2-digit'
                }),
                transactionCount: 0
            }];
        }

        const today = new Date();
        let startDate = new Date();
        
        // Determinar data inicial baseada no filtro
        switch (dateFilter) {
            case 'realtime':
                // Apenas transaÃ§Ãµes de hoje, agrupadas por hora
                startDate.setHours(0, 0, 0, 0);
                console.log('ðŸ“Š Modo Tempo Real - Saldo Atual:', currentBalance);
                return processRealtimeData(transactions, startDate, today);
                
            case '7days':
                startDate.setDate(today.getDate() - 7);
                break;
            case '30days':
                startDate.setDate(today.getDate() - 30);
                break;
            case '90days':
                startDate.setDate(today.getDate() - 90);
                break;
            case 'all':
                // ComeÃ§ar da banca inicial e processar TODAS as transaÃ§Ãµes
                console.log('ðŸ“Š Modo ALL - Banca Inicial:', initialBalance, 'Saldo Atual:', currentBalance);
                return processAllTransactions(transactions);
            default:
                startDate.setDate(today.getDate() - 7);
        }

        console.log('ðŸ“Š Filtro:', dateFilter, 'Banca Inicial:', initialBalance, 'Saldo Atual:', currentBalance);

        // Filtrar e ordenar transaÃ§Ãµes dentro do perÃ­odo
        const filteredTransactions = transactions
            .filter(tx => {
                const txDate = new Date(tx.created_at || tx.date);
                return txDate >= startDate && txDate <= today;
            })
            .sort((a, b) => new Date(a.created_at || a.date) - new Date(b.created_at || b.date));

        console.log('ðŸ“Š TransaÃ§Ãµes filtradas:', filteredTransactions.length);

        if (filteredTransactions.length === 0) {
            // Se nÃ£o hÃ¡ transaÃ§Ãµes no perÃ­odo, mostrar apenas o saldo atual
            console.log('ðŸ“Š Sem transaÃ§Ãµes no perÃ­odo, usando saldo atual:', currentBalance);
            return [{
                date: startDate.toISOString().split('T')[0],
                balance: currentBalance || 0,
                dateFormatted: startDate.toLocaleDateString('pt-BR', {
                    day: '2-digit',
                    month: '2-digit'
                }),
                transactionCount: 0
            }];
        }

        // USAR O SALDO ATUAL e VOLTAR para calcular
        // Em vez de comeÃ§ar da banca inicial e ir para frente,
        // vamos usar o saldo atual e calcular retroativamente
        
        // Agrupar transaÃ§Ãµes por data
        const dataByDate = {};
        
        filteredTransactions.forEach(tx => {
            const txDate = new Date(tx.created_at || tx.date);
            const dateKey = txDate.toISOString().split('T')[0];

            if (!dataByDate[dateKey]) {
                dataByDate[dateKey] = {
                    date: dateKey,
                    transactions: []
                };
            }

            dataByDate[dateKey].transactions.push(tx);
        });

        // Calcular o saldo do Ãºltimo dia atÃ© o primeiro
        const sortedDates = Object.keys(dataByDate).sort();
        let runningBalance = currentBalance || 0;

        console.log('ðŸ“Š Saldo inicial (atual):', runningBalance);

        // Processar de trÃ¡s para frente para ter certeza que termina no saldo atual
        for (let i = sortedDates.length - 1; i >= 0; i--) {
            const dateKey = sortedDates[i];
            const dayData = dataByDate[dateKey];
            
            // Somar transaÃ§Ãµes do dia
            let dayTotal = 0;
            dayData.transactions.forEach(tx => {
                const amount = parseFloat(tx.amount) || 0;
                if (tx.type === 'deposit' || tx.type === 'gains') {
                    dayTotal += amount;
                } else if (tx.type === 'withdraw' || tx.type === 'losses') {
                    dayTotal -= amount;
                }
            });

            // Se Ã© o Ãºltimo dia, o saldo jÃ¡ Ã© o atual
            if (i === sortedDates.length - 1) {
                dataByDate[dateKey].balance = runningBalance;
            } else {
                // Para dias anteriores, subtrair as transaÃ§Ãµes dos dias seguintes
                dataByDate[dateKey].balance = runningBalance;
            }
            
            // Voltar o saldo para o dia anterior
            if (i > 0) {
                runningBalance -= dayTotal;
            }
        }

        console.log('ðŸ“Š Saldo no inÃ­cio do perÃ­odo:', runningBalance);

        // Agora recalcular de frente para trÃ¡s com o saldo inicial correto
        runningBalance = dataByDate[sortedDates[0]].balance - dataByDate[sortedDates[0]].transactions.reduce((sum, tx) => {
            const amount = parseFloat(tx.amount) || 0;
            if (tx.type === 'deposit' || tx.type === 'gains') {
                return sum + amount;
            } else if (tx.type === 'withdraw' || tx.type === 'losses') {
                return sum - amount;
            }
            return sum;
        }, 0);

        // Processar corretamente de frente para trÃ¡s
        const chartData = sortedDates.map(dateKey => {
            const dayData = dataByDate[dateKey];
            
            dayData.transactions.forEach(tx => {
                const amount = parseFloat(tx.amount) || 0;
                if (tx.type === 'deposit' || tx.type === 'gains') {
                    runningBalance += amount;
                } else if (tx.type === 'withdraw' || tx.type === 'losses') {
                    runningBalance -= amount;
                }
            });

            return {
                date: dateKey,
                balance: runningBalance,
                dateFormatted: new Date(dateKey).toLocaleDateString('pt-BR', {
                    day: '2-digit',
                    month: '2-digit'
                }),
                transactionCount: dayData.transactions.length
            };
        });

        console.log('ðŸ“Š Saldo final no grÃ¡fico:', chartData[chartData.length - 1]?.balance);
        console.log('ðŸ“Š Dados do grÃ¡fico:', chartData);

        return chartData;
    }, [transactions, dateFilter, realtimeData, processRealtimeData, processAllTransactions, currentBalance, initialBalance]);

    // Calcular estatÃ­sticas do perÃ­odo
    const periodStats = useMemo(() => {
        if (processChartData.length === 0) {
            return {
                startBalance: 0,
                endBalance: 0,
                variation: 0,
                variationPercent: 0,
                highestBalance: 0,
                lowestBalance: 0
            };
        }

        const startBalance = processChartData[0].balance;
        const endBalance = processChartData[processChartData.length - 1].balance;
        const variation = endBalance - startBalance;
        const variationPercent = startBalance !== 0 ? (variation / startBalance) * 100 : 0;

        const balances = processChartData.map(item => item.balance);
        const highestBalance = Math.max(...balances);
        const lowestBalance = Math.min(...balances);

        return {
            startBalance,
            endBalance,
            variation,
            variationPercent,
            highestBalance,
            lowestBalance
        };
    }, [processChartData]);

    // Custom Tooltip
    const CustomTooltip = ({ active, payload }) => {
        if (active && payload && payload.length) {
            const data = payload[0].payload;
            const isRealtime = data.isRealtime;
            
            return (
                <div className={styles.customTooltip}>
                    <p className={styles.tooltipDate}>
                        {isRealtime ? `${data.dateFormatted}` : data.dateFormatted}
                    </p>
                    <div className={styles.tooltipRow}>
                        <span className={styles.tooltipLabel}>
                            <FaCoins /> Saldo:
                        </span>
                        <span className={styles.tooltipValueBalance}>
                            {formatCurrency(payload[0].value)}
                        </span>
                    </div>
                    {data.transactionCount > 0 && (
                        <div className={styles.tooltipInfo}>
                            <small>
                                {data.transactionCount} transaÃ§Ã£o(Ãµes) 
                                {isRealtime ? ' nesta hora' : ' neste dia'}
                            </small>
                        </div>
                    )}
                </div>
            );
        }
        return null;
    };

    return (
        <div className={styles.chartContainer}>
            {/* Header com filtros */}
            <div className={styles.chartHeader}>
                <div className={styles.chartTitle}>
                    <FaChartLine />
                    <h3>Performance</h3>
                    {dateFilter === 'realtime' && (
                        <span className={styles.liveIndicator}>
                            <span className={styles.liveDot}></span>
                            AO VIVO
                        </span>
                    )}
                </div>
                
                <div className={styles.dateFilters}>
                    <button
                        className={`${styles.filterButton} ${dateFilter === 'realtime' ? styles.active : ''}`}
                        onClick={() => setDateFilter('realtime')}
                    >
                        ðŸ”´ Tempo Real
                    </button>
                    <button
                        className={`${styles.filterButton} ${dateFilter === '7days' ? styles.active : ''}`}
                        onClick={() => setDateFilter('7days')}
                    >
                        7 dias
                    </button>
                    <button
                        className={`${styles.filterButton} ${dateFilter === '30days' ? styles.active : ''}`}
                        onClick={() => setDateFilter('30days')}
                    >
                        30 dias
                    </button>
                    <button
                        className={`${styles.filterButton} ${dateFilter === '90days' ? styles.active : ''}`}
                        onClick={() => setDateFilter('90days')}
                    >
                        90 dias
                    </button>
                    <button
                        className={`${styles.filterButton} ${dateFilter === 'all' ? styles.active : ''}`}
                        onClick={() => setDateFilter('all')}
                    >
                        Tudo
                    </button>
                </div>
            </div>

            {/* EstatÃ­sticas do PerÃ­odo */}
            <div className={styles.statsRow}>
                <div className={styles.statCard}>
                    <span className={styles.statLabel}>
                        <FaCoins /> Saldo Inicial
                    </span>
                    <span className={styles.statValueBalance}>
                        {formatCurrency(periodStats.startBalance)}
                    </span>
                    <span className={styles.statAvg}>
                        {dateFilter === 'realtime' ? 'InÃ­cio do dia' : 'InÃ­cio do perÃ­odo'}
                    </span>
                </div>

                <div className={styles.statCard}>
                    <span className={styles.statLabel}>
                        <FaCoins /> Saldo {dateFilter === 'realtime' ? 'Atual' : 'Final'}
                    </span>
                    <span className={styles.statValueBalance}>
                        {formatCurrency(periodStats.endBalance)}
                    </span>
                    <span className={styles.statAvg}>
                        {dateFilter === 'realtime' ? 'Agora' : 'Final do perÃ­odo'}
                    </span>
                </div>

                <div className={styles.statCard}>
                    <span className={styles.statLabel}>
                        <MdTrendingUp /> VariaÃ§Ã£o {dateFilter === 'realtime' ? 'Hoje' : ''}
                    </span>
                    <span className={`${styles.statValueNet} ${
                        periodStats.variation >= 0 ? styles.positive : styles.negative
                    }`}>
                        {periodStats.variation >= 0 ? '+' : ''}{formatCurrency(periodStats.variation)}
                    </span>
                    <span className={`${styles.statAvg} ${
                        periodStats.variation >= 0 ? styles.positive : styles.negative
                    }`}>
                        {periodStats.variation >= 0 ? 'â–²' : 'â–¼'} {Math.abs(periodStats.variationPercent).toFixed(2)}%
                    </span>
                </div>
            </div>

            {/* GrÃ¡fico */}
            <div className={styles.chartWrapper}>
                {processChartData.length > 0 ? (
                    <ResponsiveContainer width="100%" height={350}>
                        <LineChart 
                            data={processChartData}
                            margin={{ top: 5, right: 20, left: 10, bottom: 5 }}
                        >
                            <CartesianGrid strokeDasharray="3 3" stroke="rgba(255,255,255,0.1)" />
                            <XAxis 
                                dataKey="dateFormatted" 
                                stroke="#999"
                                style={{ fontSize: '12px' }}
                            />
                            <YAxis 
                                stroke="#999"
                                style={{ fontSize: '12px' }}
                                tickFormatter={(value) => `R$ ${value.toFixed(0)}`}
                            />
                            <Tooltip content={<CustomTooltip />} />
                            <Legend 
                                wrapperStyle={{ 
                                    paddingTop: '20px',
                                    fontSize: '14px'
                                }}
                            />
                            <Line 
                                type="monotone" 
                                dataKey="balance" 
                                stroke="#FFD700" 
                                strokeWidth={3}
                                name="Saldo Atual"
                                dot={{ fill: '#FFD700', r: 4 }}
                                activeDot={{ r: 7, fill: '#FDB931' }}
                            />
                        </LineChart>
                    </ResponsiveContainer>
                ) : (
                    <div className={styles.emptyChart}>
                        <FaChartLine size={48} />
                        <p>Nenhum dado disponÃ­vel para o perÃ­odo selecionado</p>
                        <small>Adicione transaÃ§Ãµes para visualizar a evoluÃ§Ã£o do saldo</small>
                    </div>
                )}
            </div>
        </div>
    );
};

export default PerformanceChart;